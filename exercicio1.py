# Definindo as variáveis conforme o trecho de código fornecido
INDICE = 13  # Valor do índice para o loop
SOMA = 0    # Variável para armazenar a soma dos números
K = 0       # Variável de controle do loop

# Enquanto K for menor que INDICE, faça:
while K < INDICE:
    # Incrementando K em 1 a cada iteração
    K = K + 1
    # Adicionando o valor de K à variável SOMA
    SOMA = SOMA + K

# Imprimindo o valor final de SOMA
print(SOMA)
